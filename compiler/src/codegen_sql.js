function generateRulesDDL(rulesDef) {
  const lines = [];
  lines.push(`-- Business rules: ${rulesDef.name}`);

  for (const rule of rulesDef.rules) {
    switch (rule.kind) {
      case 'DenyRule':
        lines.push(generateDenyTrigger(rule, rulesDef.name));
        break;
      case 'RequireRule':
        lines.push(generateRequireTrigger(rule, rulesDef.name));
        break;
      case 'LimitRule':
        lines.push(generateLimitDDL(rule, rulesDef.name));
        break;
    }
    lines.push('');
  }

  return lines.join('\n');
}

function generateDenyTrigger(rule, rulesName) {
  const tableName = toSnakeCase(rule.model) + 's';
  const opMap = {
    'delete': 'DELETE', 'update': 'UPDATE', 'create': 'INSERT',
    'archive': 'UPDATE', 'restore': 'UPDATE',
  };
  const op = opMap[rule.verb] || 'DELETE';
  const fnName = `deny_${rule.verb}_${tableName}`;

  return `-- Rule: deny ${rule.verb} ${rule.model}
CREATE OR REPLACE FUNCTION ${fnName}()
RETURNS TRIGGER AS $$
BEGIN
  RAISE EXCEPTION '${rule.verb} on ${rule.model} is denied by ${rulesName}';
  RETURN NULL;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER ${fnName}_trigger
  BEFORE ${op} ON ${tableName}
  FOR EACH ROW EXECUTE FUNCTION ${fnName}();`;
}

function generateRequireTrigger(rule, rulesName) {
  const tableName = toSnakeCase(rule.field.model) + 's';
  const fnName = `require_${rule.requirement}_${tableName}_${rule.field.field}`;
  const condition = `NEW.${rule.field.field} ${rule.op} ${formatSqlValue(rule.value)}`;

  return `-- Rule: ${rule.field.model}.${rule.field.field} ${rule.op} ${rule.value} requires ${rule.requirement}
CREATE OR REPLACE FUNCTION ${fnName}()
RETURNS TRIGGER AS $$
BEGIN
  IF ${condition} THEN
    IF NOT EXISTS (
      SELECT 1 FROM approvals
      WHERE entity_type = '${rule.field.model}'
        AND entity_id = NEW.id
        AND approval_type = '${rule.requirement}'
        AND status = 'approved'
    ) THEN
      RAISE EXCEPTION '${rule.field.model} with ${rule.field.field} ${rule.op} ${rule.value} requires ${rule.requirement}';
    END IF;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER ${fnName}_trigger
  BEFORE INSERT OR UPDATE ON ${tableName}
  FOR EACH ROW EXECUTE FUNCTION ${fnName}();`;
}

function generateLimitDDL(rule, rulesName) {
  const sourceTable = toSnakeCase(rule.field.model) + 's';
  const targetEntity = rule.entity;
  const trackingTable = `${sourceTable}_${targetEntity}_limits`;
  const fnName = `enforce_${targetEntity}_limit_${sourceTable}`;

  const periodMap = {
    'second': 'second', 'minute': 'minute', 'hour': 'hour',
    'day': 'day', 'week': 'week', 'month': 'month',
    'seconds': 'second', 'minutes': 'minute', 'hours': 'hour',
    'days': 'day', 'weeks': 'week', 'months': 'month',
  };
  const period = periodMap[rule.timeUnit] || 'month';

  return `-- Rule: ${rule.field.model}.${rule.field.field} = ${rule.value} limits ${rule.limit} ${targetEntity}/${rule.timeUnit}
CREATE TABLE IF NOT EXISTS ${trackingTable} (
  source_id UUID NOT NULL,
  period_start TIMESTAMPTZ NOT NULL,
  count INTEGER NOT NULL DEFAULT 0,
  PRIMARY KEY (source_id, period_start)
);

CREATE OR REPLACE FUNCTION ${fnName}()
RETURNS TRIGGER AS $$
DECLARE
  v_source_value TEXT;
  v_count INTEGER;
  v_period_start TIMESTAMPTZ;
BEGIN
  SELECT ${rule.field.field} INTO v_source_value
  FROM ${sourceTable}
  WHERE id = NEW.${toSnakeCase(rule.field.model)}_id;

  IF v_source_value = ${formatSqlValue(rule.value)} THEN
    v_period_start := date_trunc('${period}', NOW());

    INSERT INTO ${trackingTable} (source_id, period_start, count)
    VALUES (NEW.${toSnakeCase(rule.field.model)}_id, v_period_start, 1)
    ON CONFLICT (source_id, period_start)
    DO UPDATE SET count = ${trackingTable}.count + 1
    RETURNING count INTO v_count;

    IF v_count > ${rule.limit} THEN
      RAISE EXCEPTION '${rule.field.model} with ${rule.field.field} = ${rule.value} is limited to ${rule.limit} ${targetEntity} per ${rule.timeUnit}';
    END IF;
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER ${fnName}_trigger
  BEFORE INSERT ON ${targetEntity}
  FOR EACH ROW EXECUTE FUNCTION ${fnName}();`;
}

function formatSqlValue(value) {
  if (typeof value === 'number' || !isNaN(Number(value))) return value;
  return `'${value}'`;
}
